package sensorControlService;

import com.pi4j.io.gpio.GpioPinDigitalInput;
import com.pi4j.io.gpio.Pin;
import commData.CommData;


public class DigitalCDS {

	//- -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	//- Member Variable
	//- -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	private final static String 			 TAG 		 = "DigitalCDS";

	private final GpioPinDigitalInput  cdsPin;
	
	
	//- -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	//- Constructor Method
	//- -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	public DigitalCDS(Pin cdsPin) {
		super();
		this.cdsPin = CommData.GPIO.provisionDigitalInputPin(cdsPin);
		  
		CommData.log(TAG, "init OK");
	}	
	  
	//- -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	//- Member Method
	//- -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
  public boolean getLight(){
	
	   return this.cdsPin.isHigh();
   }
    
}
