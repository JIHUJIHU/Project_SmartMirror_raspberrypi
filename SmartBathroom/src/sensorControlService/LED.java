package sensorControlService;

import com.pi4j.io.gpio.GpioPinDigitalOutput;
import com.pi4j.io.gpio.Pin;
import commData.CommData;


public class LED {

	//- -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	//- Member Variable
	//- -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	private final static String 				TAG 				= "LED";
  private final GpioPinDigitalOutput  ledPin;
            
  
	//- -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	//- Constructor Method 
	//- -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	public LED(Pin ledPin) {
			super();

		  this.ledPin = CommData.GPIO.provisionDigitalOutputPin( ledPin );
		  this.ledPin.low();
		  
		  CommData.log(TAG, "ultrasonic init OK");
	}

	//- -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	//- Member Method 
	//- -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	public void controlLED(boolean value){
			if(value == true)
					this.ledPin.high();
			else
					this.ledPin.low();
	}

	public void onLED(){
				this.ledPin.high();
	}
	
	public void offLED(){
				this.ledPin.low();
	}
	
}
